def _register_field(mapping: dict[str, str], field: str, label: str) -> None:
    normalized = re.sub(r"([a-z0-9])([A-Z])", r"\1_\2", field.strip())
    snake = re.sub(r"[_\-\s]+", "_", normalized).lower().strip("_")
    for key in {field.lower(), snake, snake.replace("_", "")}:
        if key:
            mapping[key] = label


def _build_segment_map() -> dict[str, str]:
    mapping = dict(_BASE_SEGMENT_MAP)
    for field, label in FIELD_TRANSLATIONS:
        _register_field(mapping, field, label)
    return mapping


SEGMENT_MAP: dict[str, str] = _build_segment_map()
